package com.altria.imagewatcher.entity;

import java.io.Serializable;

/**
 * Created by altria on 17-8-23.
 */

public class ImageViewInfo implements Serializable{
    private int left;
    private int top;
    private int width;
    private int height;

    public ImageViewInfo(int left, int top, int width, int height) {
        this.left = left;
        this.top = top;
        this.width = width;
        this.height = height;
    }

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
